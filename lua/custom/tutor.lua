local function file_has_content(path)
  local stat = vim.uv.fs_stat(path)
  return stat ~= nil and stat.type == 'file' and stat.size > 0
end

local function tutor_paths()
  local root = vim.fs.joinpath(vim.fn.stdpath 'data', 'kickstart-tutor')
  local java_root = vim.fs.joinpath(root, 'java-drill')
  local java_src = vim.fs.joinpath(java_root, 'src', 'main', 'java', 'com', 'kickstart', 'tutor')
  local java_test = vim.fs.joinpath(java_root, 'src', 'test', 'java', 'com', 'kickstart', 'tutor')

  return {
    root = root,
    readme = vim.fs.joinpath(root, 'README.md'),
    movement = vim.fs.joinpath(root, '00-movement.txt'),
    editing = vim.fs.joinpath(root, '10-editing.txt'),
    notes = vim.fs.joinpath(root, 'notes', 'navigation-notes.md'),
    git_demo = vim.fs.joinpath(root, '40-git-demo.txt'),
    java_root = java_root,
    pom = vim.fs.joinpath(java_root, 'pom.xml'),
    java_app = vim.fs.joinpath(java_src, 'App.java'),
    java_greeter = vim.fs.joinpath(java_src, 'Greeter.java'),
    java_test = vim.fs.joinpath(java_test, 'GreeterTest.java'),
  }
end

local function split_lines(text)
  return vim.split(text:gsub('\r\n', '\n'), '\n', { plain = true, trimempty = false })
end

local function write_file(path, text)
  vim.fn.mkdir(vim.fs.dirname(path), 'p')
  vim.fn.writefile(split_lines(text), path)
end

local function run(cmd, cwd)
  local result = vim.system(cmd, { cwd = cwd, text = true }):wait()
  if result.code ~= 0 then
    local message = result.stderr ~= '' and result.stderr or result.stdout
    error(string.format('Command failed: %s\n%s', table.concat(cmd, ' '), message))
  end
  return result
end

