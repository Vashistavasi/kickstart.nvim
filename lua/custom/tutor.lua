  add_checkbox(lines, 'Use `*`, `#`, `n`, and `N` on `Greeter` and `terminal-pro`.')
  add_checkbox(lines, "Set mark `a` on `MARK-A`, jump to `MARK-B`, then return with `'a` and `` `a``.")
  add_checkbox(lines, 'Create a jumplist trail with `gg`, `10G`, `/MARK-B`, and travel back with `<C-o>` then forward with `<C-i>`.')
  add('')
  add('## 10-20 Editing Fluency')
  add('> Open editing drill', function()
    open_file(paths.editing, paths.root)
  end)
  add('  ' .. paths.editing)
