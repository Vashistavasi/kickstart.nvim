  add_checkbox(lines, 'Use `<leader><leader>` to switch buffers after opening at least three files from this workspace.')
  add_checkbox(lines, 'Use `<leader>sf` to fuzzy-find `Greeter.java` from the workspace root.')
  add_checkbox(lines, 'Use `<leader>sg` to live-grep for `clockwork`, `ember-fox`, or `terminal-pro`.')
