local function ensure_workspace()
  local paths = tutor_paths()
  if file_has_content(paths.readme)
    and file_has_content(paths.movement)
    and file_has_content(paths.editing)
    and file_has_content(paths.git_demo)
    and file_has_content(paths.java_app)
    and vim.uv.fs_stat(vim.fs.joinpath(paths.root, '.git'))
  then
    return paths
  end
  return build_workspace()
end

local function open_file(path, cwd)
  local paths = ensure_workspace()
  if not file_has_content(path) then
    build_workspace()
  end
  vim.cmd.tabnew()
  if cwd then
    vim.cmd.tcd(vim.fn.fnameescape(cwd))
  elseif vim.startswith(path, paths.root) then
    vim.cmd.tcd(vim.fn.fnameescape(paths.root))
  end
  vim.cmd.edit(vim.fn.fnameescape(path))
end

local function set_cwd(path)
  vim.cmd.tcd(vim.fn.fnameescape(path))
  vim.notify('Changed tab working directory to ' .. path, vim.log.levels.INFO)
end

local function add_checkbox(lines, text)
  table.insert(lines, '- [ ] ' .. text)
end

