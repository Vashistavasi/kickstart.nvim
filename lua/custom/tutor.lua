  add_checkbox(lines, 'Use `grn` to rename `Greeter` or `enthusiasticMessageFor`, then undo or rename it back cleanly.')
  add_checkbox(lines, 'Use `<leader>jo` in `App.java` to organize imports and remove the unused `ArrayList` import.')
  add_checkbox(lines, 'Use `<leader>jc` in `App.java` to debug the main class from the terminal.')
  add_checkbox(lines, 'Use `<leader>jn` inside `GreeterTest.java` to debug the nearest test method.')
  add('')
  add('## Daily Loop')
  add_checkbox(lines, 'Spend 5 minutes on motion-only editing in `00-movement.txt`.')
  add_checkbox(lines, 'Spend 5 minutes on search, buffers, Neo-tree, and symbols in the workspace.')
  add_checkbox(lines, 'Spend 5 minutes on one real refactor or debug session in the Java project.')

  local buf = vim.api.nvim_create_buf(false, true)
  vim.api.nvim_set_current_buf(buf)
  vim.api.nvim_buf_set_lines(buf, 0, -1, false, lines)
  vim.bo[buf].buftype = 'nofile'
  vim.bo[buf].bufhidden = 'wipe'
  vim.bo[buf].swapfile = false
  vim.bo[buf].filetype = 'markdown'
  vim.bo[buf].modifiable = false
  vim.api.nvim_buf_set_name(buf, 'kickstart://tutor/' .. buf)
  vim.api.nvim_buf_add_highlight(buf, -1, 'Title', 0, 0, -1)

  state.actions[buf] = actions
  vim.api.nvim_create_autocmd('BufWipeout', {
    buffer = buf,
    callback = function()
      state.actions[buf] = nil
    end,
  })

  vim.keymap.set('n', '<CR>', function()
    local current = vim.api.nvim_win_get_cursor(0)[1]
    local action = state.actions[buf][current]
    if action then
      action()
    end
  end, { buffer = buf, desc = 'Kickstart Tutor: run action' })

  vim.keymap.set('n', 'x', function()
    local current = vim.api.nvim_win_get_cursor(0)[1]
    local line = vim.api.nvim_buf_get_lines(buf, current - 1, current, false)[1]
    if not line then
      return
    end
    local replacement
    if line:find('%- %[ %] ', 1, false) == 1 then
      replacement = line:gsub('%- %[ %] ', '- [x] ', 1)
    elseif line:find('%- %[x%] ', 1, false) == 1 then
      replacement = line:gsub('%- %[x%] ', '- [ ] ', 1)
    end
    if replacement then
      vim.bo[buf].modifiable = true
      vim.api.nvim_buf_set_lines(buf, current - 1, current, false, { replacement })
      vim.bo[buf].modifiable = false
    end
  end, { buffer = buf, desc = 'Kickstart Tutor: toggle checkbox' })

  vim.keymap.set('n', 'R', function()
    build_workspace()
    render_tutor()
  end, { buffer = buf, desc = 'Kickstart Tutor: rebuild workspace' })

  vim.keymap.set('n', 'q', function()
    vim.cmd.close()
  end, { buffer = buf, desc = 'Kickstart Tutor: close buffer' })
end

function M.open()
  vim.cmd.tabnew()
  render_tutor()
end

function M.reset_workspace()
  build_workspace()
end

function M.setup()
  if vim.g.kickstart_tutor_loaded then
    return
  end
  vim.g.kickstart_tutor_loaded = true

  vim.api.nvim_create_user_command('KickstartTutor', function()
    M.open()
  end, { desc = 'Open the Kickstart interactive tutor' })

  vim.api.nvim_create_user_command('KickstartTutorReset', function()
    M.reset_workspace()
    vim.notify('Kickstart Tutor workspace rebuilt', vim.log.levels.INFO)
  end, { desc = 'Rebuild the Kickstart tutorial workspace' })
end

return M
