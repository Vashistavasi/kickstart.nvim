  add_checkbox(lines, 'Record a macro that changes `pending` to `done`, then replay it down the TODO block.')
  add_checkbox(lines, 'Use `mini.surround`: `saiw)` on `surround`, then `sr)\"` or `sd)` to replace or remove it.')
  add('')
  add('## 20-30 Project Navigation')
